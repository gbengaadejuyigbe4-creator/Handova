const translations: Record<Language, Translations> = { en, fr, ar };

export function t(lang: Language, key: keyof Translations, ...args: number[]): string {
  const val = translations[lang][key];
  if (typeof val === "function") return (val as (n: number, total: number) => string)(args[0], args[1]);
  return val as string;
}

export function getDir(lang: Language): "ltr" | "rtl" {
  return lang === "ar" ? "rtl" : "ltr";
}
